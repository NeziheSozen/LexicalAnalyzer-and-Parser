
public class Terminal {

	public String terminal;
	public String type;
	public int row;
	public int col;
	public int level;
	
	public Terminal(String terminal, String type,int row,int col, int level){
		
		this.terminal = terminal;
		this.type = type;
		this.level = level;
		this.row = row;
		this.col = col;
		
	}
	
	
}
